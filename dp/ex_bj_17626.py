N = int(input())

d = [0] * 50001
for n in range(1, 223): # int(50000 ** 0.5)
    d[n**2] = 1

for n in range(2, N+1):
    mn = 123456789
    for i in range(1, int(n ** 0.5)+1):
        if mn > d[n - i**2]:
            mn = d[n - i**2]
    d[n] = mn + 1

print(d[N])
